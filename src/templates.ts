import type { TerrainObject, UnitToken, DrawingObject } from './types';
import type { BoardState } from './types';
import { v4 as uuidv4 } from 'uuid';

export interface ScenarioTemplate {
  id: string;
  name: string;
  description: string;
  emoji: string;
  /** Tag line shown under name */
  subtitle: string;
  terrain: () => TerrainObject[];
  /** Optional: full board state (map image, dimensions, etc.) */
  board?: Partial<BoardState>;
  /** Optional: pre-placed units */
  units?: () => UnitToken[];
  /** Optional: pre-placed drawings */
  drawings?: () => DrawingObject[];
  /** Canvas width the coordinates were authored at (for scaling) */
  savedCanvasWidth?: number;
}

function t(
  shape: TerrainObject['shape'],
  points: number[],
  tags: TerrainObject['tags'] = ['blocks_los'],
  color = '#6b7280',
  opacity = 0.85,
): TerrainObject {
  return { id: uuidv4(), shape, points, tags, color, opacity, locked: false, layerId: 'terrain' };
}

// ─── Helper to build a centered rect from (cx, cy, w, h) ────────────────────
function cr(cx: number, cy: number, w: number, h: number, tags?: TerrainObject['tags'], color?: string) {
  const x0 = cx - w / 2, y0 = cy - h / 2;
  const x1 = cx + w / 2, y1 = cy + h / 2;
  return t('rect', [x0, y0, x1, y0, x1, y1, x0, y1], tags, color);
}

